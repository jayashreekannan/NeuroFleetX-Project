package Javaexercises;
public class Datatypeprogram2 {
    public static void main(String[] args) {
        int num = 100;
        double d = num; 
        double pi = 3.14159;
        int intPi = (int) pi; 

        System.out.println("Integer to Double: " + d);
        System.out.println("Double to Integer: " + intPi);
    }

}
